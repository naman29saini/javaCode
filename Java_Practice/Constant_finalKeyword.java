public class Constant_finalKeyword {
    public static void main(String[] arg)
    {
        //final keyword is used to make any varible cosntant.
        float Pi=8.14F;
        System.out.println(Pi);
        Pi=8.92F;
        System.out.println(Pi);



        //final keyword
       final  float PI=3.14F;
        System.out.println(PI);
        // PI=7.78F; //it will throw an error beacuse we cannot assign vale to the final .
    }
}
