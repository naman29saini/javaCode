public class Casting {
    public static void main(String[] arg)
    {
        //implicit casting
        double num1=234.67;
        double num2=543;
        System.out.println(num1+num2);
        int a2=34;

        //explicit casting
        int a3=a2+(int)56.2;
        System.out.println(a3);
    }
}
